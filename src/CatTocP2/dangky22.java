    private void lbSginupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbSginupMouseClicked
        // TODO add your handling code here:
        String EMAIL_REGEX = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";

        if (!txtrePass.getText().equalsIgnoreCase(txtPass.getText())) {
            lblrepass.setText("Hai mật khẩu không giống nhau!");
            return;
        } else if (Txtuser.getText().equalsIgnoreCase("")) {
            lblrepass.setText("Bạn chưa nhập username?");
            return;
        } else if (txtPass.getText().equalsIgnoreCase("")) {
            lblrepass.setText("Bạn chưa nhập password!");
            return;
        } else if (txtrePass.getText().equalsIgnoreCase("")) {
            lblrepass.setText("Bạn chưa nhập Re-password!");
            return;
        } else if (txtHoten.getText().equalsIgnoreCase("")) {
            lblrepass.setText("Bạn chưa nhập họ tên!");
            return;
        } else if (txtdiachi.getText().equalsIgnoreCase("")) {
            lblrepass.setText("Bạn chưa nhập địa chỉ!");
            return;
        } else if (txtemail.getText().equalsIgnoreCase("")) {
            lblrepass.setText("Bạn chưa nhập email!");
            return;
        } else if (!txtemail.getText().matches(EMAIL_REGEX)) {
            lblrepass.setText("Bạn nhập ko đúng định dạng email!");
            return;
        } else {

            try {
                PreparedStatement ps = cn.prepareStatement("INSERT INTO USES\n"
                        + "VALUES (?,?,?)   \n"
                        + "INSERT INTO KHACHHANG\n"
                        + "VALUES (?,?,?,?,?,?) ");
                ps.setString(1, Txtuser.getText());
                ps.setString(2, txtPass.getText());
                ps.setString(3, "KH");
                ps.setString(4, Txtuser.getText());
                ps.setString(5, txtHoten.getText());
                ps.setString(6, cbbChonnam.getSelectedItem() + "-" + cbbChonthang.getSelectedItem() + "-" + cbbChonngay.getSelectedItem());
                ps.setString(7, txtsodt.getText());
                ps.setString(8, txtdiachi.getText());
                ps.setString(9, txtemail.getText());

                if (ps.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "BẠN ĐÃ ĐĂNG KÝ THÀNH CÔNG!");
                    new dangnhap22().setVisible(true);
                    this.dispose();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }

    }//GEN-LAST:event_lbSginupMouseClicked

